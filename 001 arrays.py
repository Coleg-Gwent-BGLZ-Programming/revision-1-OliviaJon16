# Example: Working with arrays (lists in Python)
numbers = [10, 20, 30, 40, 50]

# Access elements
print("First element:", numbers[0])

# Add an element
numbers.append(60)
print("After append:", numbers)

# Remove an element
numbers.remove(30)
print("After remove:", numbers)

# Iterate through array
for num in numbers:
    print(num)

